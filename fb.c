// property tag buffer for mailbox — must be 16-byte aligned
static unsigned int __attribute__((aligned(16))) mbox_buf[36];

int fb_init(unsigned int width, unsigned int height){
	int i = 0;
	mbox_buf[i++] = 0;              // [0] buffer size (filled at end)
	mbox_buf[i++] = 0;              // [1] request code

	// set physical display size
	mbox_buf[i++] = 0x00048003;     // tag: set physical w/h
	mbox_buf[i++] = 8;              // value buffer size
	mbox_buf[i++] = 0;              // request
	mbox_buf[i++] = width;          // [5] width
	mbox_buf[i++] = height;         // [6] height

	// set virtual display size
	mbox_buf[i++] = 0x00048004;     // tag: set virtual w/h
	mbox_buf[i++] = 8;
	mbox_buf[i++] = 0;
	mbox_buf[i++] = width;          // [10] virt width
	mbox_buf[i++] = height;         // [11] virt height

	// set virtual offset (0,0)
	mbox_buf[i++] = 0x00048009;     // tag: set virtual offset
	mbox_buf[i++] = 8;
	mbox_buf[i++] = 0;
	mbox_buf[i++] = 0;              // [15] x offset
	mbox_buf[i++] = 0;              // [16] y offset

	// set depth (32 bits per pixel)
	mbox_buf[i++] = 0x00048005;     // tag: set depth
	mbox_buf[i++] = 4;
	mbox_buf[i++] = 0;
	mbox_buf[i++] = 32;             // [20] bits per pixel

	// set pixel order (RGB)
	mbox_buf[i++] = 0x00048006;     // tag: set pixel order
	mbox_buf[i++] = 4;
	mbox_buf[i++] = 0;
	mbox_buf[i++] = 1;              // [24] 1 = RGB

	// allocate framebuffer
	mbox_buf[i++] = 0x00040001;     // tag: allocate buffer
	mbox_buf[i++] = 8;
	mbox_buf[i++] = 0;
	mbox_buf[i++] = 4096;           // [28] alignment
	mbox_buf[i++] = 0;              // [29] returned size

	// get pitch
	mbox_buf[i++] = 0x00040008;     // tag: get pitch
	mbox_buf[i++] = 4;
	mbox_buf[i++] = 0;
	mbox_buf[i++] = 0;              // [33] returned pitch

	mbox_buf[i++] = 0;              // end tag
	mbox_buf[0] = i * 4;            // total buffer size in bytes

	if(!mbox_call(MBOX_CH_PROP, mbox_buf))
		return -1;

	// check we got a framebuffer
	if(mbox_buf[28] == 0)
		return -1;

	// GPU returns bus address — convert to ARM physical address
	// bus address 0xC0000000+ maps to ARM physical 0x00000000+
	unsigned long fb_addr = mbox_buf[28] & 0x3FFFFFFF;
	fb_ptr = (volatile unsigned int *)fb_addr;
	fb_size = mbox_buf[29];
	fb_w = width;
	fb_h = height;
	fb_p = mbox_buf[33]; // pitch in bytes

	// QEMU raspi4b renders the framebuffer horizontally mirrored (x=0 = right edge).
	// Compensate by enabling mirror mode at runtime — cannot rely on static initializer
	// because boot.s does not copy .data from ROM to RAM before calling kernel_main().
	fb_mirror = 1;

	return 0;
}

unsigned int fb_width(void)  { return fb_w; }
unsigned int fb_height(void) { return fb_h; }
unsigned int fb_pitch(void)  { return fb_p; }

void fb_set_mirror(int m){ fb_mirror = m; }
int  fb_get_mirror(void){ return fb_mirror; }

void fb_put_pixel(unsigned int x, unsigned int y, unsigned int color){
	if(x >= fb_w || y >= fb_h) return;
	if(fb_mirror) x = fb_w - 1 - x;
	unsigned int offset = (y * fb_p / 4) + x;
	fb_ptr[offset] = color;
}

void fb_clear(unsigned int color){
	unsigned int total = fb_p / 4 * fb_h;
	for(unsigned int i = 0; i < total; i++)
		fb_ptr[i] = color;
}

void fb_fill_rect(unsigned int x, unsigned int y, unsigned int w, unsigned int h, unsigned int color){
	// when mirrored, the rectangle is placed at the flipped x position
	unsigned int px = (fb_mirror && fb_w >= x + w) ? (fb_w - x - w) : x;
	for(unsigned int row = y; row < y + h && row < fb_h; row++){
		unsigned int off = (row * fb_p / 4) + px;
		for(unsigned int col = 0; col < w && px + col < fb_w; col++){
			fb_ptr[off + col] = color;
		}
	}
}

void fb_put_char(unsigned int x, unsigned int y, char c, unsigned int fg, unsigned int bg){
	int idx = c - 32;
	if(idx < 0 || idx > 94) idx = 0; // unprintable -> space

	const unsigned char *glyph = font8x8[idx];
	for(int row = 0; row < 8; row++){
		unsigned char bits = glyph[row];
		for(int col = 0; col < 8; col++){
			unsigned int color = (bits & (0x80 >> col)) ? fg : bg;
			fb_put_pixel(x + col, y + row, color);
		}
	}
}

void fb_put_char_scaled(unsigned int x, unsigned int y, char c, unsigned int scale, unsigned int fg, unsigned int bg){
	int idx = c - 32;
	if(idx < 0 || idx > 94) idx = 0;
	const unsigned char *glyph = font8x8[idx];
	for(int row = 0; row < 8; row++){
		unsigned char bits = glyph[row];
		for(int col = 0; col < 8; col++){
			unsigned int color = (bits & (0x80 >> col)) ? fg : bg;
			for(unsigned int dy = 0; dy < scale; dy++)
				for(unsigned int dx = 0; dx < scale; dx++)
					fb_put_pixel(x + col * scale + dx, y + row * scale + dy, color);
		}
	}
}

void fb_put_string_scaled(unsigned int x, unsigned int y, const char *s, unsigned int scale, unsigned int fg, unsigned int bg){
	while(*s){
		fb_put_char_scaled(x, y, *s, scale, fg, bg);
		x += 8 * scale;
		s++;
	}
}

void fb_put_string(unsigned int x, unsigned int y, const char *s, unsigned int fg, unsigned int bg){
	while(*s){
		if(x + 8 > fb_w){
			x = 0;
			y += 8;
		}
		if(y + 8 > fb_h) break;
		fb_put_char(x, y, *s, fg, bg);
		x += 8;
		s++;
	}
}

// --- console with scrolling ---

void fb_console_init(unsigned int fg, unsigned int bg){
	con_fg = fg;
	con_bg = bg;
	con_cx = 0;
	con_cy = 0;
	con_cols = fb_w / 8;
	con_rows = fb_h / 8;
	con_enabled = 1;
}

void fb_console_enable(void)  { con_enabled = 1; }
void fb_console_disable(void) { con_enabled = 0; }

static void console_scroll(void){
	// move all rows up by one character row (8 pixels)
	unsigned int row_pixels = 8;
	unsigned int *dst = (unsigned int *)fb_ptr;
	unsigned int *src = (unsigned int *)fb_ptr + (row_pixels * fb_p / 4);
	unsigned int count = (fb_h - row_pixels) * fb_p / 4;
	for(unsigned int i = 0; i < count; i++)
		dst[i] = src[i];

	// clear the last row
	unsigned int last_y = (con_rows - 1) * 8;
	fb_fill_rect(0, last_y, fb_w, 8, con_bg);
}

void fb_console_putc(char c){
	if(!fb_ptr || !con_enabled) return;

	if(c == '\n' || c == '\r'){
		con_cx = 0;
		con_cy++;
		if(con_cy >= con_rows){
			console_scroll();
			con_cy = con_rows - 1;
		}
		return;
	}
	if(c == '\b'){
		if(con_cx > 0){
			con_cx--;
			fb_put_char(con_cx * 8, con_cy * 8, ' ', con_fg, con_bg);
		}
		return;
	}
	if(c == '\t'){
		con_cx = (con_cx + 4) & ~3;
		if(con_cx >= con_cols){
			con_cx = 0;
			con_cy++;
			if(con_cy >= con_rows){
				console_scroll();
				con_cy = con_rows - 1;
			}
		}
		return;
	}

	fb_put_char(con_cx * 8, con_cy * 8, c, con_fg, con_bg);
	con_cx++;
	if(con_cx >= con_cols){
		con_cx = 0;
		con_cy++;
		if(con_cy >= con_rows){
			console_scroll();
			con_cy = con_rows - 1;
		}
	}
}

void fb_console_puts(const char *s){
	while(*s)
		fb_console_putc(*s++);
}

void fb_console_clear(void){
	fb_clear(con_bg);
	con_cx = 0;
	con_cy = 0;
}
